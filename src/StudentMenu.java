    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        try{

            stmt = conn.createStatement();
            String userEmail = Email.getText();
            String userId = sid.getText();
            

            String sql = "SELECT * FROM student WHERE email='"+userEmail+"' AND sid = '"+userId+"' ";
          
            rs = stmt.executeQuery(sql);
            if(rs.next()){
                
                profile.setVisible(true);
                name.setText(rs.getString("name"));
                email.setText(rs.getString("email"));
                bd.setText(rs.getString("Birthdate"));
                addr.setText(rs.getString("Address"));
                phone.setText(rs.getString("PhoneNum"));
                inst.setText(rs.getString("Institution"));
            }else{
                JOptionPane.showMessageDialog(null,"Account Not Found!");
            }

        }catch(HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_loginActionPerformed

