    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try {
            
            stmt = conn.createStatement();
            String userEmail = Email.getText();
            String userId = sid.getText();
            
            String tui = fees.getText();
            double price = Double.parseDouble(tui);
            if (price > 0.0) {
                double fee = 0.0;
                String desc1 = "-";
                String info = "PAID";

                String sql = "UPDATE student SET Tuition_Fees = '"+fee+"', Tuition_Information = '"+desc1+"' WHERE email='"+userEmail+"' AND sid = '"+userId+"' ";

                stmt.executeUpdate(sql);

                fees.setText(Double.toString(fee));
                info1.setText("-");
                desc.setText(info);
                paymentMethod.clearSelection();

                JOptionPane.showMessageDialog(null, "Payment Success");
            } else {
                paymentMethod.clearSelection();
                JOptionPane.showMessageDialog(null, "Tuition Already Paid!");
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

